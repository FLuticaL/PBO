#Menghitung Volume dan luas Permukaan limas segiempat
#variabel
p = 12
T = 18
s = 15

#Rumus
Volume = 1/3*p*p*T
LuasPermukaan = (p*p) + (2*p*s)

#output
print("panjang sisi segiempat : ", p)
print("tinggi limas : ", T)
print("sisi tegak : ", s)
print("Volume : ", Volume)
print("LuasPermukaan : ", LuasPermukaan)